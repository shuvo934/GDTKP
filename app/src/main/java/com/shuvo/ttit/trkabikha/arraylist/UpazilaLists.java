package com.shuvo.ttit.trkabikha.arraylist;

public class UpazilaLists {

    private String ddId;
    private String thanaName;

    public UpazilaLists(String ddId, String thanaName) {
        this.ddId = ddId;
        this.thanaName = thanaName;
    }

    public String getDdId() {
        return ddId;
    }

    public void setDdId(String ddId) {
        this.ddId = ddId;
    }

    public String getThanaName() {
        return thanaName;
    }

    public void setThanaName(String thanaName) {
        this.thanaName = thanaName;
    }
}
